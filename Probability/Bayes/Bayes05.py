"""
Задача 5. Два кубика (честный и нечестный)
Кубик 1: честный (P_six_d1 = 1/6) - вероятность выпадения 6
Кубик 2: нечестный (P_six_d2 = 1/2) - вероятность выпадения 6
Выбор кубика равновероятен: P_d1 = P_d2 = 0.5.

Найти: P_d2_given_six — вероятность, что это кубик 2, если выпала шестёрка.
"""
P_d1 = 0.5
P_d2 = 0.5
P_six_d1 = 1 / 6
P_six_d2 = 1 / 2

P_six = P_six_d1 * P_d1 + P_six_d2 * P_d2
P_d2_given_six = (P_six_d2 * P_d2) / P_six

print(f"P(D2 | Six) = {P_d2_given_six:.4f}")  # ≈ 0.75

